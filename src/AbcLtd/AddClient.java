    private void jCheckBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCheckBox1ActionPerformed
      try {
            String select = "select * from clienttable where c_id=" + cId.getText();
            stm = log.con.createStatement();
            rs = stm.executeQuery(select);
            while (rs.next()) {
                cId.setText(rs.getString("c_id"));
                cName.setText(rs.getString("c_name"));
                String gn = rs.getString("sex");
                if (gn.equals("Male")) {
                    male.setSelected(true);
                    female.setSelected(false);
                } else {
                    male.setSelected(false);
                    female.setSelected(true);
                }

                dob.setText(rs.getString("dob"));
                father.setText(rs.getString("fathers_name"));
                jComboBox_occupation.setSelectedItem(rs.getString("occupation"));
                mother.setText(rs.getString("mothers_name"));
                phone.setText(rs.getString("phone"));
                email.setText(rs.getString("email"));
                address.setText(rs.getString("address"));
            }
        } catch (SQLException ex) {
//            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jCheckBox1ActionPerformed

