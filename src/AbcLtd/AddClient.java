    public void createTable() {
        try {
            DatabaseMetaData dmd = log.con.getMetaData();
            ResultSet rs = dmd.getTables(null, null, "ClientTable", null);
            if (rs.next()) {
                //do nothing
            } else {
                String create_table = "create table ClientTable("
                        + "c_id int(5) NOT NULL primary key,"
                        + "c_name varchar(25),"
                        + "sex varchar(8),"
                        + "dob date,"
                        + "fathers_name varchar(25),"
                        + "mothers_name varchar(25),"
                        + "occupation varchar(15),"
                        + "phone varchar(12),"
                        + "email varchar(25),"
                        + "address varchar(50)"
                        + ")";
                pstm = log.con.prepareStatement(create_table);
                pstm.executeUpdate();
                JOptionPane.showMessageDialog(null, "ClientTable Created");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

