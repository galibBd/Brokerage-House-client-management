    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        portfolio_table();
        try {
            String insert = "insert into clienttable values(?,?,?,?,?,?,?,?,?,?)";
            pstm = log.con.prepareStatement(insert);
            pstm.setString(1, cId.getText());
            pstm.setString(2, cName.getText());
            String g;
            if (male.isSelected() == true) {
                g = "Male";
            } else {
                g = "Female";
            }
            pstm.setString(3, g);
            pstm.setString(4, dob.getText());
            pstm.setString(5, father.getText());
            pstm.setString(6, mother.getText());
            pstm.setString(7, jComboBox_occupation.getSelectedItem().toString());
            pstm.setString(8, phone.getText());
            pstm.setString(9, email.getText());
            pstm.setString(10, address.getText());

            int into = pstm.executeUpdate();
            if (into > 0) {
                JOptionPane.showMessageDialog(null, "Data Inserted");
                clear();
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

    }//GEN-LAST:event_btn_addActionPerformed

